				this.$forceUpdate();
			},
																		
																																							// 下拉变化
			yonghupingfenChange(e) {
				this.yonghupingfenIndex = e.target.value
				this.ruleForm.yonghupingfen = this.yonghupingfenOptions[this.yonghupingfenIndex]
			},
																														
																																				tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																																															
			getUUID () {
				return new Date().getTime();
